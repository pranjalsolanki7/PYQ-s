// 📘 OPEN PART
// ===============================

function openITPart(part){

let html = `
<div class="chapter-wrap">
<div class="chapter-title">📘 ${itData[part].title}</div>

<div class="card-container">
`;

Object.keys(itData[part].chapters).forEach(chapter => {

html += `
<div class="chapter-card" onclick="openITChapter(\`${part}\`, \`${chapter}\`)">
${chapter}
</div>
`;

});

html += `
</div>

<button onclick="loadITTopics()" class="back-btn">⬅ Back</button>
</div>
`;

document.getElementById("questionArea").innerHTML = html;

}


