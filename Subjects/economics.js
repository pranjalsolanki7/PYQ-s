function formatText(text) {
  if (!text) return "";
  return text.replace(/\n/g, "<br>");
}


// ===============================
// 📊 SHOW ECONOMICS CHAPTER CARDS
// ===============================

function loadEconomicsChapters() {
  let html = `
    <div class="chapter-wrap">
      <div class="chapter-title">📊 Economics – Select Chapter</div>
      <div class="card-container">
  `;

  Object.keys(economicsData).forEach(chapter => {
    html += `
      <div class="chapter-card" onclick="openEconomicsChapter('${chapter}')">
        ${chapter}
      </div>
    `;
  });

  html += `
      </div>
    </div>
  `;

  document.getElementById("questionArea").innerHTML = html;
}


// ===============================
// 📘 OPEN ECONOMICS CHAPTER
// ===============================

function openEconomicsChapter(chapter) {
  let qHtml = `
    <div class="chapter-wrap">
      <div class="chapter-title">📘 ${chapter}</div>
  `;

  economicsData[chapter].forEach((item, index) => {
    qHtml += `
      <div class="question">
        <h4 style="color:#1e40af;">Q${index + 1} (${item.year})</h4>

        <p><b>Question:</b><br>${formatText(item.q.main)}</p>
    `;

    // ---- QUESTION PARTS (a, b, c) ----
    if (item.q.parts) {
      qHtml += `<ul>`;
      for (let key in item.q.parts) {
        qHtml += `<li><b>(${key})</b> ${formatText(item.q.parts[key])}</li>`;
      }
      qHtml += `</ul>`;
    }

    // ---- ANSWER ----
    qHtml += `<p><b>Answer:</b></p><ul>`;

    for (let key in item.a) {
      qHtml += `<li><b>(${key})</b><ul>`;
      item.a[key].forEach(point => {
        qHtml += `<li>${formatText(point)}</li>`;
      });
      qHtml += `</ul></li>`;
    }

    qHtml += `
        </ul>
      </div>
    `;
  });

  qHtml += `
      <button onclick="loadEconomicsChapters()" class="back-btn">⬅ Back to Chapters</button>
    </div>
  `;

  document.getElementById("questionArea").innerHTML = qHtml;
}


