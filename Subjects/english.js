function openEnglishTopic(topic) {

let html = `
<div class="chapter-wrap">
<div class="chapter-title">📗 ${topic}</div>
`;

html += `<div class="concept-container">`;

englishData[topic].concepts.forEach((concept, index) => {

html += `
<div class="concept-card" onclick="toggleConcept(${index})">

<div class="concept-title">
✨ ${concept.title}
</div>

<div class="concept-content" id="concept-${index}">

<ul>
${concept.points.map(p => `<li>${p}</li>`).join("")}
</ul>

<div class="example-box">
${concept.examples.map(e => `<div>👉 ${e}</div>`).join("")}
</div>

</div>

</div>
`;

});

html += `</div>`;

// ===== PRACTICE =====
const practice = englishData[topic].practice;

html += `
<div class="question">

<h3 style="color:#22c55e;">🔥 Practice (CBSE Style)</h3>

<b>Easy</b>
<ol>
${practice.easy.map(q => `<li>${q}</li>`).join("")}
</ol>

<b>Hard</b>
<ol>
${practice.hard.map(q => `<li>${q}</li>`).join("")}
</ol>






</div>

<button onclick="loadEnglishTopics()" class="back-btn">⬅ Back</button>
</div>
`;

document.getElementById("questionArea").innerHTML = html;
}


// ===============================
// TOGGLE (WORKING)
// ===============================
function toggleConcept(id){
let el = document.getElementById("concept-" + id);

if(el.style.display === "block"){
el.style.display = "none";
}else{
el.style.display = "block";
}
}

function loadEnglishTopics() {

  let html = `
    <div class="chapter-wrap">
      <div class="chapter-title">📘 English Grammar</div>
      <div class="card-container">
  `;

  Object.keys(englishData).forEach(topic => {
    html += `
      <div class="chapter-card" onclick="openEnglishTopic('${topic}')">
        ${topic}
      </div>
    `;
  });

  html += `
      </div>
    </div>
  `;

  document.getElementById("questionArea").innerHTML = html;
}
