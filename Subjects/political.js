function loadCivicsChapters() {
  let html = `
    <div class="chapter-wrap">
      <div class="chapter-title">🏛️ Political Science – Select Chapter</div>
      <div class="card-container">
  `;

  Object.keys(civicsData).forEach(chapter => {
    html += `
      <div class="chapter-card" onclick="openCivicsChapter('${chapter}')">
        ${chapter}
      </div>
    `;
  });

  html += `
      </div>
    </div>
  `;

  document.getElementById("questionArea").innerHTML = html;
}

// ===============================
// OPEN POLITICAL SCIENCE CHAPTER
// ===============================
function openCivicsChapter(chapter) {
  let qHtml = `
    <div class="chapter-wrap">
      <div class="chapter-title">📘 ${chapter}</div>
  `;

  civicsData[chapter].forEach((item, i) => {
    qHtml += `
      <div class="question">
        <h4 style="color:#1e40af;">Q${i + 1} (${item.year})</h4>
        <p><b>Question:</b><br>${formatText(item.q.main)}</p>
    `;

    // -------- Question Parts --------
    if (item.q.parts) {
      qHtml += `<ul>`;
      for (let key in item.q.parts) {
        qHtml += `<li><b>(${key})</b> ${formatText(item.q.parts[key])}</li>`;
      }
      qHtml += `</ul>`;
    }

    // -------- Answer --------
    qHtml += `<p><b>Answer:</b></p><ul>`;

    for (let key in item.a) {
      qHtml += `<li><b>(${key})</b><ul>`;
      item.a[key].forEach(point => {
        qHtml += `<li>${formatText(point)}</li>`;
      });
      qHtml += `</ul></li>`;
    }

    qHtml += `
        </ul>
      </div>
    `;
  });

  qHtml += `
      <button onclick="loadCivicsChapters()" class="back-btn">⬅ Back to Chapters</button>
    </div>
  `;

  document.getElementById("questionArea").innerHTML = qHtml;
}

