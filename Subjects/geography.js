// ===============================
// SHOW QUESTIONS
// ===============================

function openGeographyChapter(chapter) {
  let qHtml = `
    <div class="chapter-wrap">
      <div class="chapter-title">📘 ${chapter}</div>
  `;

  geographyData[chapter].forEach((item, i) => {
    qHtml += `
      <div class="question">
        <h4 style="color:#1e40af;">Q${i + 1} (${item.year})</h4>

        <p><b>Question:</b> ${item.q.main}</p>
        <ul>
    `;

    // ---- QUESTION PARTS ----
    if (item.q.parts) {
      for (let key in item.q.parts) {
        qHtml += `<li><b>(${key})</b> ${item.q.parts[key]}</li>`;
      }
    }

    qHtml += `
        </ul>
        <p><b>Answer:</b></p>
        <ul>
    `;

    // ---- ANSWER PARTS ----
    for (let key in item.a) {
      qHtml += `<li><b>(${key})</b>
        <ul>
      `;
      item.a[key].forEach(point => {
        qHtml += `<li>${point}</li>`;
      });
      qHtml += `
        </ul>
      </li>`;
    }

    qHtml += `</ul>`;

    // ---- DIAGRAM ----
    if (item.diagram) {
      qHtml += `
        <div class="diagram-box">
          <p><b>Diagram / Map:</b></p>
          <img src="${item.diagram}" alt="diagram">
        </div>
      `;
    }

    qHtml += `</div>`;
  });

  qHtml += `</div>`;
  document.getElementById("questionArea").innerHTML = qHtml;
}
